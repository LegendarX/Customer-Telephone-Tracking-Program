    public void tabloyu_goster(){
        jTable2.setModel(tablo);
        tablo.setColumnIdentifiers(new String [] {"Adı Soyadı","Telefonu","Marka Model","Durum","Fiyat","Açıklama","Not","ID"});
        
        //BURASI KONTROL EDİYOR TABLODA EKLİ OLAN VAR İSE TEMİZLİYOR SONR TEKRAR EKİYOR
        while (tablo.getRowCount()>0){
            for(int i=0; i<tablo.getRowCount(); i++){
                tablo.removeRow(i);
            }
        }
        
        try{
            con = Connectionz.getConnection();
            st=con.createStatement();
            rs=st.executeQuery("SELECT * FROM MUSTERI_BILGILERII");
            while(rs.next())
            {
                tablo.addRow(new Object[] {rs.getString("MUSTERI_ADI_SOYADI"),rs.getString("MUSTERI_TELEFONU"),rs.getString("MARKA_MODEL"),rs.getString("DURUM"),rs.getString("FIYAT"),rs.getString("ARIZA_ACIKLAMASI"),rs.getString("NOT"),rs.getString("ID")});
            }
        }
        catch (Exception ex){
            System.out.println("hata "+ex);
        }
    }
    
