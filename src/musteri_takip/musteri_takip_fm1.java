    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // Mouse Click
        // get the model from the jtable
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        
        // get the selected row index
        int selectedRowIndex = jTable2.getSelectedRow();
        
        // set the selected row data into jtextfields
        jTextField_adi_soyadi.setText(model.getValueAt(selectedRowIndex, 0).toString());
        jTextField_telefonu.setText(model.getValueAt(selectedRowIndex, 1).toString());
        jTextField_marka.setText(model.getValueAt(selectedRowIndex, 2).toString());
        jComboBox_durum.setSelectedItem(model.getValueAt(selectedRowIndex, 3).toString());
        jTextField_fiayat.setText(model.getValueAt(selectedRowIndex, 4).toString());
        jTextArea_ariza.setText(model.getValueAt(selectedRowIndex, 5).toString());
        jTextArea_not.setText(model.getValueAt(selectedRowIndex, 6).toString());
        jTextField_id.setText(model.getValueAt(selectedRowIndex, 7).toString());
    }//GEN-LAST:event_jTable2MouseClicked

